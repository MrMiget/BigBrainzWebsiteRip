                      <p align="left"><a name="WebGL" id="WebGL"></a><span class="title">WebGL/Chromebook Performance</span></p>
                      <p align="left">&nbsp;</p>
                      <p align="left"><strong>Quick Check</strong></p>
                      <p align="left">To evaluate WebGL performance we strongly recommend a simple &quot;Quick Check.&quot; This involves walking into a lab and simply clicking on the link on roughly as many computers as you expect to be loading concurrently and observe whether the game loads well enough. This will give you an excellent, real-world assessment. Alternately, you can consider the following and make an educated guess. Note up front that Firefox will currently reload significantly faster than other browsers.</p>
                      <p align="left">&nbsp;</p>
                        <p align="left" class="text14"><strong>Startup Time</strong><br />
                          Startup  time is dictated by <strong>Download Time</strong> <strong>+ Load Time</strong>. That is, how long it takes  to download the content from the internet PLUS the time it takes the computer  to then read all that code, load everything into memory, and start drawing it  on the screen. We&rsquo;re fairly accurate at showing &ldquo;Download&rdquo; time on screen. Load  time is more complex and can include some things that are hard for us to track,  measure, or display such as parsing code, or instantiating objects and such. Download  time will depend on per-computer bandwidth. Load time will depend computer  speed.</p>
                        <p align="left">&nbsp;</p>
                        <p align="left" class="text14"><strong>Download Time</strong><br />
                          As  mentioned, download time is determined by per-computer bandwidth:</p>
                        <p align="left" class="text14">&nbsp;</p>
                        <p align="left" class="text14">At 1 Mbps  our game will download in 2 minutes. <br />
                          At 1 Gbps  it will download in 1/10th second.</p>
                        <p align="left" class="text14">&nbsp;</p>
                        <p align="left" class="text14">Individual  computer bandwidth is determined by the school&rsquo;s total bandwidth divided by the  number of users concurrently downloading content. Note that once we download a  game we have almost zero bandwidth impact—until we load the next game.</p>
                        <p align="left" class="text14">&nbsp;</p>
                        <p align="left" class="text14">Example  #1. If a school has a Gigabit connection and one student, they will download  the game in 1/10th of a second. If the same school decides to start  1,000 students simultaneously at 10:00, it will take roughly 1,000 times as  long for everyone to download, or two minutes. </p>
                        <p align="left" class="text14">&nbsp;</p>
                        <p align="left" class="text14">Example  #2. If a school only has a 5Mbps connection and one student, they will download  the game in 24 seconds. If the same school decides to start 30 students  simultaneously, it will take roughly 30 times longer than that to download.</p>
                        <p align="left">&nbsp;</p>
                        <p align="left" class="text14"><strong>Caching</strong><br />
                          Ideally  the browser will cache the assets when it downloads them so that future  downloads will be instant. If downloading is not fast enough the first time,  try doing it a second time to see if caching is working. If it is, the download  portion of startup should be instantaneous. </p>
                        <p align="left" class="text14">&nbsp;</p>
                        <p align="left" class="text14">There  are two types of caching. The core game is cached by the browser. We have  little control over it. Generally the browser will try to hold on to often-used  content so we SHOULD be cached effectively. But there are some practices that can  set a browser to automatically clear its cache constantly. </p>
                        <p align="left" class="text14">&nbsp;</p>
                        <p align="left" class="text14">Once you  load the main game, each individual game is cached in a different location  (Unless you&rsquo;re on an ARM processor). It&rsquo;s possible that some schools may have  configurations that would wipe this out as well.</p>
                        <p align="left">&nbsp;</p>
                        <p align="left" class="text14"><strong>Loading Time</strong><br />
                          Once a  computer has the content cached or downloaded, it needs to parse the code and  instantiate all the objects. A blazing fast workstation will load a game  instantly. The slowest chromebooks and cloudbooks we&rsquo;ve tested load in about 30  seconds.</p>
                        <p align="left">&nbsp;</p>
                        <p align="left" class="text14"><strong>Browser Choice</strong><br />
                          WebGL  browser features are evolving very rapidly. As an example, version 29 of  Firefox ran our game at 1 frame per second, while version 42 ran it at 40  frames per second. Expect performance to increase significantly throughout 2016  and 2017.</p>
                        <p align="left" class="text14">As of  today Firefox caches parsed code and therefore can generally load the game up  to six times faster than other browsers. This is obviously not an option on  Chromebooks, but we strongly recommend using Firefox anywhere else.</p>
                        <p align="left">&nbsp;</p>
                        <p align="left" class="text14"><strong>Performance Tracking</strong><br />
                          We are  developing a tool that should let allow some rudimentary remote troubleshooting.  So far it will let us view download and load times.</p>
                        <p align="left">&nbsp;</p>
                        <p align="left" class="text14"><strong>Wireless Networks</strong><br />
                          Wireless routers and access points are  notorious for not delivering the full performance that a school expects them to.  If the access point is too far away its connection can degrade or be lost  entirely. Also keep in mind that different wireless technologies have <a href="https://en.wikipedia.org/wiki/IEEE_802.11">different maximum throughput</a>. </p>
                        <p align="center" class="text14">802.11b   = 11  Mbps Max (2.4GHz)<br />
                          802.11g   = 54  Mbps Max (2.4GHz)<br />
                          802.11n   = 600 Mbps Max (2.4GHz &amp; 5GHz)<br />
                        801.11ac = 1300 Mbps Max. (5GHz)</p>
